package basics;

public class Conditionalstatements2 {

	public static void main(String[] args) {
		//if else if ladder
		
		int i=7;
		if(i==1) {
			System.out.println("Im Monday");
		}else if (i==2) {
			System.out.println("Im tuesday");
		}else if(i==3) {
			System.out.println("Im wednesday");
		}else if(i==4) {
			System.out.println("Im thursday");
		}else {
			System.out.println("Not a valid day");
		}
		//Find the biggest number between 2 numbers
		int a=10;
		int b=20;
		if(a>b) {
			System.out.println("a is greater");
		}
		else {
			System.out.println("b is greater");
		}
		//Declare 3 numbers find biggest number from the three numbers using if else if ladder
		
		
		
		
	}
}
